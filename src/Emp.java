
public class Emp {
	public int id;
	public String email;
	public String phone;
	public String hire_date;
	public String job_id;
	public int salary;
	public String lastName;
	public String countryName;
	public int commission_pct;
	public int department_id;
	public int manager_id;
	@Override
	public String toString() {
		return "Emp [id=" + id + ", email=" + email + ", phone=" + phone + ", hire_date=" + hire_date + ", job_id="
				+ job_id + ", salary=" + salary + ", lastName=" + lastName + ", countryName=" + countryName
				+ ", commission_pct=" + commission_pct + ", department_id=" + department_id + ", manager_id="
				+ manager_id + "]";
	}
	



}
